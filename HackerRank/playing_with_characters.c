// Solution 


#include<stdio.h>
int main(void)
{
    char word1[100],word2[100],word3[100];
    scanf("%[^\n]%*c",word1);
    scanf("%[^\n]%*c",word2);
    scanf("%[^\n]%*c",word3);
    printf("%s \n",word1);
    printf("%s \n",word2);
    printf("%s \n",word3);
    

}

